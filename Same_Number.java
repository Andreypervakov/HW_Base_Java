public class Same_Number {
    public static void main(String[] args) {
        int number = 57;

        int d = number / 10;
        int e = number % 10;

        int sum = d+e;
        int mulip = d*e;

        System.out.println(d);
        System.out.println(e);
        System.out.println(sum);
        System.out.println(mulip);
    }
}

